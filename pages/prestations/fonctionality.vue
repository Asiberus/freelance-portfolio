        La mise en place d’un système de gestion utilisateur est utile lorsque vous souhaitez
        restreindre l’accès à certaines parties de votre site. Son implémentation comporte :
        <ul>
            <li>Une page de création/édition/suppression d’un utilisateur</li>
            <li>Une page de login</li>
            <li>La restriction de pages ou de contenus à certains utilisateurs</li>
            <li>
                La création de rôles ayant chacun des permissions (administrateur, manageur, etc.)
            </li>
        </ul>
        En tant que propriétaire du site, vous aurez un accès total à la gestion des utilisateurs.
        Si votre site est domicilié en Europe, il est également obligatoire de respecter les règles
        du RGPD (Règlement Général sur la Protection des Données). Ces règles encadrent la gestion
        et la conservation des données utilisateurs (
