    <reference-item title="Gestion de contenu" reference="FCT-002">
        Un CMS (Content Management System) permet la création, la modification et la suppression du
        contenu de votre site. Ce système est utile pour tout site dont le contenu change
        régulièrement (par ex : site vitrine, blog, mise en avant de nouveautés, etc.). Il permet
        d’actualiser le contenu sans intervention de ma part. L’accès au CMS nécessite un système de
        gestion utilisateur pour restreindre la modification aux utilisateurs autorisés.
    </reference-item>
    <reference-item title="Système de paiement en ligne" reference="FCT-003">
        Intégration d’un système de paiement en ligne. Cela permet au visiteur de procéder à un
        paiement sur votre site. Le paiement peut être lié à une réservation, l’achat d’un article
        ou tout autre service que vous proposez. L’intégration peut utiliser Stripe, PayPal, ou
        toute autre entreprise avec laquelle vous avez habitude de travailler. Des coûts sont
        généralement à prévoir avec l’utilisation de ces services.
        <br />
        Il est fortement conseillé de certifier le site avec HTTPS, les données bancaires de
        paiement sont des données sensibles qui nécessitent une forte sécurité.
    </reference-item>
    <reference-item title="Gestion commentaire" reference="FCT-004">
        Permet la création de commentaires sous les articles de votre site. Cela permet d’augmenter
        la contribution et l’engagement de vos visiteurs. Un commentaire peut être fait de manière
        anonyme ou bien uniquement par des utilisateurs authentifiés. Une page d’administration sera
        dédiée à la modération des commentaires.
    </reference-item>
    <reference-item title="Gestion e-mail" reference="FCT-005">
        Intégration d’un serveur e-mail. Celui-ci vous permet d’envoyer et de recevoir des e-mails.
        Il est utile d’implémenter ce type de système pour des sites permettant la création
        d’utilisateurs ou proposants de la vente d’articles en ligne. Les e-mails peuvent être
        envoyés de manière automatique ou bien manuelle.
    </reference-item>
    <reference-item title="Campagne d'e-mail" reference="FCT-006">
        Si vous souhaitez communiquer efficacement sur votre entreprise, il est possible
        d’implémenter une plateforme permettant l’envoi de campagnes d’e-mail. Cela vous permet de
        créer et d’envoyer rapidement un e-mail à l’ensemble de vos clients, ou de cibler
        spécifiquement une partie de votre audience pour une communication plus fine. Cette
        plateforme permet également la gestion d’une newsletter. Il est possible, grâce à des outils
        d’automatisation, de planifier l’envoi de campagnes et de suivre leurs performances à l’aide
        de statistiques. <br /><br />
        Le tarif de cette prestation est composé d’un prix fixe pour la création de la plateforme et
        d’un abonnement correspondant au coût du service e-mail.
    </reference-item>
