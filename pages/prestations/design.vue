    <reference-item title="Charte graphique" reference="DES-001">
        Le développement d’une charte graphique est une étape importante dans la réalisation d’un
        projet. Elle représente un ensemble de règles qui définissent chaque élément visuel de votre
        site, comme les couleurs, les typographies, les espacements, les formes, etc. Elle permet
        une uniformisation de chacune des pages. La charte graphique a pour objectif de représenter
        au mieux votre identité visuelle et de s’accorder aux messages et aux valeurs que vous ou
        votre entreprise souhaite transmettre. Il est donc nécessaire pas négliger cette étape de la
        réalisation de votre site. <br />
        Un document contenant l’ensemble des règles de la charte graphique vous sera fourni.
    </reference-item>
    <reference-item title="Réalisation de maquettes" reference="DES-002">
        La réalisation de maquettes permet de valider l’apparence de votre site en amont du
        développement. Celles-ci représentent chacune des pages de manière simplifiée afin de mettre
        en évidence chaque élément important de votre design. Il est également possible de simuler
        un affichage sur mobile, tablette ou ordinateur portable afin d’observer le comportement de
        votre site sur ces appareils. Cela permet d’assurer une expérience utilisateur optimale.
        <br />
        Plusieurs réunions sont en général nécessaires afin de réaliser les maquettes, mais, une
        fois celles-ci finalisées, le développement de votre site en sera fortement accéléré.
    </reference-item>
    <reference-item title="Design depuis un modèle" reference="DES-003">
        Réalisation du design de votre site depuis un modèle. Celui-ci comprend l’ensemble des
        composants classiques d’un site web (titre, menu, bouton, formulaire) et sera ensuite
        personnalisé afin d’y intégrer votre charte graphique et vos éléments visuels (logo, images,
        vidéos). Le choix du modèle sera fait selon une liste proposée par mes soins. Cette solution
        assure un développement rapide de votre site. Le design depuis un modèle est plus adapté
        pour des sites peu complexes, comme des sites vitrine. Il est généralement compliqué de
        trouver un modèle pertinent pour un site de gestion de société ou bien un e-shop, ceux-ci
        étant très fortement liés aux fonctionnalités que vous souhaitez intégrer.
    </reference-item>
    <reference-item title="Design original" reference="DES-004">
        Réalisation du design de votre site sans prendre appui sur un modèle existant. Cela permet
        de créer un site répondant au mieux à votre identité visuelle. Le comportement et
        l’apparence de chaque composant seront définis afin de correspondre à l’expérience que vous
        souhaitez proposer à vos visiteurs. Un site proposant un design original se démarque
        généralement de ses concurrents. La réalisation de votre design se fera en plusieurs
        réunions (si nécessaire).
    </reference-item>
    <reference-item title="Configuration de thèmes" reference="DES-005">
        Il est possible de proposer plusieurs thèmes aux visiteurs de votre site. Un thème
        représente une palette de couleurs et définit l’apparence globale de votre site. Les
        couleurs seront appliquées aux titres, aux menus, aux liens, ainsi qu’à tout autre élément
        visuel. Les deux thèmes les plus courants sont les thèmes clairs et sombres, mais il est
        possible de réaliser autant de thèmes que vous souhaitez.
    </reference-item>
    <reference-item title="Version mobile standard" reference="DES-006">
        Cette prestation comprend le développement de votre site en respectant l’expérience
        utilisateur sur mobile. De nos jours, un très grand nombre d’utilisateurs accèdent à
        Internet depuis un mobile. Il peut être donc intéressant d’adapter votre site pour ces
        appareils. La réalisation d’un design mobile représente l’adaptation de chaque élément
        visuel pour que ceux-ci soient lisibles et accessibles pour un usage sur un écran de petite
        taille. <br />
        La version standard définit trois tailles (mobile, tablette, ordinateurs) : 360 x 640, 720 x
        1080 et 1080 x 1920.
    </reference-item>
    <reference-item title="Version mobile avancée" reference="DES-007">
        Cette prestation comprend le développement de votre site en portant une attention
        particulière à une utilisation sur mobile. Elle est conseillée pour les sites dont
        l’audience majoritaire est sur mobile ou tablette. Cette prestation comprend l’ensemble des
        points de la version standard ainsi que l’implémentation d’interactions (zoom, utilisation
        de plusieurs doigts, etc.) et de services (GPS, Bluetooth) spécifiques aux mobiles. Cela
        permet à votre site de s’approcher d’une application installée et de profiter au mieux des
        possibilités offertes par des appareils de type mobiles ou tablettes.
    </reference-item>
    <reference-item title="Intégration web" reference="DES-008">
        L’intégration web est une étape indispensable qui vise à transformer le design établi en
        pages interprétables par un navigateur. La durée et le coût de cette prestation sont très
        relatifs et dépendent du nombre et de la complexité des pages que vous souhaitez pour votre
        site. Par exemple une animation ou une interaction spécifique à un composant augmente le
        temps de réalisation de l’intégration.
    </reference-item>
    <reference-item title="Rafraichissement du design" reference="DES-009">
        Cette prestation comprend un rafraîchissement et une amélioration du design de votre site.
        Cela peut représenter une amélioration mineure et ciblée sur certains éléments visuels ou
        bien une amélioration globale de votre site. Le rafraîchissement peut se faire suivant les
        tendances actuelles en termes de design ou bien suivant votre volonté. Il est également
        possible d’uniformiser l’ensemble de votre site, si ce dernier a été réalisé à des périodes
        et par des personnes différentes.
    </reference-item>
